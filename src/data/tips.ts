/** Get a random focus tip */
export function getRandomTip(): string {
  return FOCUS_TIPS[Math.floor(Math.random() * FOCUS_TIPS.length)];
}

/** Get a random hadith */
export function getRandomHadith(): { text: string; source: string } {
  return QURAN_HADITHS[Math.floor(Math.random() * QURAN_HADITHS.length)];
}

/** Get tip of the day (consistent throughout the day) */
export function getDailyTip(): string {
  const today = new Date();
  const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000);
  return FOCUS_TIPS[dayOfYear % FOCUS_TIPS.length];
}

